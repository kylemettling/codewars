function appendArrays(arr1, arr2) {
  return arr1.concat(arr2);
}

appendArrays([1, 2], [2, 4]);
appendArrays([1, 2], [3, 4]);
appendArrays(["this"], ["that"]);
appendArrays(["a", "B"], ["c", "D"]);
appendArrays([1, 2], [1]);
